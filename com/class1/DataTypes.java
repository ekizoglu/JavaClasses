public class DataTypes {

	public static void main(String[] args) {
	
		//PRIMITIVE DATA TYPES
		
		//NUMERIC PRIMITIVE
		byte bMin=-128;
		byte bMax= 127;
		
		short sMin= -32768; 
		short sMax= 32767;
		
		int iMin= -767788;
		int iMax= 767787; 
		
		//for large numbers syntax requires "l" at the end upper or lower case
		long l=5765765757576l; 
		long l1= 768787555875L; 
		
		//for numbers with floating points for float need "f" at the end
		float f = 12.78f; 
		double d= 14.131241245125215; 
		
		//NON NUMERIC PRIMITIVE
		boolean hungry = true; 
		boolean rain = false; 
		
		char container= 'A'; 
		
		
		
		
		
		
		
	}

}
