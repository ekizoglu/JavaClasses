package com.class2;

public class ModulusOperator {
	
	public static void main(String[] args) {
		float num1= 12.5f; 
		float num2=3.5f; 
		
		
		
		int number1 = 13; 
		int number2 = 2; 
		int divOfIntegers=number1/number2; 
		
		System.out.println(divOfIntegers);
		
		double d=12; 
		System.out.println(d);
		
		int f1= 15;
		int f2 = 2; 
		double div= f1/f2; 
		System.out.println(div);
		
		//Operator precedence
		// '*', '/' takes precedence over '+'. '-'
		// '%' takes precedence over '+' , '-'
		
		
	}

}
