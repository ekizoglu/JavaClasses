package com.class30;

public class InterfaceNotes {
	/*
	 * Interface is like a class except you can only declare methods and variables
	 * 	meaning that you cannot implement the methods. You can only have 
	 * unimplemented (abstract)	methods
	 * Naming convention: Adjectives (classes are nouns)
	 * Interfaces are a collection of public static final (constant) variables
	 * 	and public abstract methods.
	 * 
	 * Interfaces --> collection of public static final variables & public abstract methods
	 * Using interfaces we can achieve abstraction
	 * Interfaces allow multiple inheritance (multiple parents) Without interfaces not possible
	 * 
	 * Class cannot be a parent for an interface, because it is a set of guides, tools and functionality
	 * that need to be implemented in the class implementing the interface
	 */
}
